class Tournament{

	constructor(id, result, gamesWon, gamesLost, pointsWon, pointsLost, gameHistory){

		this.id = id;
		this.result = result;
		this.gamesWon = gamesWon;
		this.gamesLost = gamesLost;
		this.pointsWon = pointsWon;
		this.pointsLost = pointsLost;
		this.gameHistory = gameHistory;
	}
}